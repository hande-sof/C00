#include <unistd.h>

void	ft_print_combn(int n)
{
	char c;

	while (n > 0)
	{
		c = '0';
		while (c <= '9')
		{
			write(1, &c, 1);
			c++;
		}
		n--;
		ft_print_combn(n);
	}
}
