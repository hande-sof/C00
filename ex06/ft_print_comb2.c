#include <unistd.h>

void	last_num(void)
{
	while (c[3] >= '9')
	{
		while (c[4] >= '9')
		{
			write(1, &c, 5);
			c[4]++;
		}
		c[3]++;
	}
}

 void    first_num(void)
 {
	 while (c[0] >= '9')
	 {
		 while (c[1] >= '9')
		 {
			 last_num();
			 c[1]++;
		 }
		 c[0]++;
	 }
 }

void	ft_print_comb2(void)
{
	char c[4];

	c[0] = '0';
	c[1] = '0';
	c[2] = ' ';
	c[3] = '0';
	c[4] = '1';
	first_num();
}
