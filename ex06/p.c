#include <unistd.h>

void	ft_print_comb2(void)
{
	char c[5];

	c[0] = '0';
	c[2] = ' ';
	c[4] = '1';

	while(c[0] <= '9')
	{
		c[1] = '0';
		while(c[1] <= '9')
		{
			c[3] = '0';
			while(c[3] <= '9')
			{
				c[4] = '0';
				if (c[1] >= '1')
				{
					c[4] = c[1] + 1;
				}
				while (c[4] <= '9')
				{
					write(1, &c, 5);
					write(1, ", ", 2);
					c[4]++;
				}
				c[3]++;
			}
			c[1]++;
		}
		c[0]++;
	}
}
